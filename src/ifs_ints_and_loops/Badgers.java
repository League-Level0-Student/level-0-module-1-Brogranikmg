package ifs_ints_and_loops;

public class Badgers {
	public static void main(String[] args) {

			for (int i = 0; i < 4; i++) {
				for (int j = 0; j < 10; j++) {
					System.out.print("Badger, ");
				}
				System.out.println();
				for (int j = 0; j < 2; j++) {
					System.out.print("Mushroom, ");
				}
				System.out.println();
			}
			System.out.print("SNAAAAAAAAAAKE!");
	}
}
