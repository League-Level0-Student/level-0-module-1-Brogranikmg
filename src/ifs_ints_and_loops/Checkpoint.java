package ifs_ints_and_loops;

public class Checkpoint {
	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		
		for (int j = 75; j >= 50; j--) {
			System.out.println(j);
		}
		
		for (int k = 1; k <= 10; k += 2) {
			System.out.println(k);
		}
	}
}
